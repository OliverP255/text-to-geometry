#!/usr/bin/env python3
"""
Read agent/experiments/outputs/by_model/*/results.json and optional accuracy_ratings.json,
then write BENCHMARK_RATINGS.md and benchmark_aggregate.json.

Metrics:
  - Validation: fraction of prompts where generated map() passes wgsl_validator.
  - Prompt fidelity: mean 1–5 score from accuracy_ratings.json (0 = no output); band means B1..B5.

Usage:
  .venv/bin/python3 agent/experiments/aggregate_benchmark_ratings.py
"""

from __future__ import annotations

import json
import statistics
from pathlib import Path

_ROOT = Path(__file__).resolve().parent
_BY_MODEL = _ROOT / "outputs" / "by_model"
_OUT_MD = _ROOT / "BENCHMARK_RATINGS.md"
_OUT_JSON = _ROOT / "benchmark_aggregate.json"

_BAND_KEYS = (
    "band1_steps_dims_1to10",
    "band2_dims_no_steps_11to20",
    "band3_no_dims_21to30",
    "band4_vague_31to40",
    "band5_organic_41to46",
)


def _grade(pct: float) -> str:
    if pct >= 90:
        return "A"
    if pct >= 75:
        return "B"
    if pct >= 50:
        return "C"
    if pct >= 25:
        return "D"
    return "F"


def _load_fidelity(sub: Path) -> dict | None:
    path = sub / "accuracy_ratings.json"
    if not path.is_file():
        return None
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return None
    summary = data.get("summary") or {}
    bands_in = summary.get("by_band") or {}
    bands_out: dict[str, float | None] = {}
    for key in _BAND_KEYS:
        b = bands_in.get(key) or {}
        m = b.get("mean")
        bands_out[key] = round(float(m), 2) if m is not None else None
    dist = summary.get("score_distribution") or {}
    n0 = int(dist.get("0", 0))
    mean = summary.get("mean_score")
    return {
        "fidelity_rater": data.get("rater"),
        "fidelity_mean": round(float(mean), 2) if mean is not None else None,
        "fidelity_no_output_count": n0,
        "fidelity_bands": bands_out,
    }


def main() -> None:
    rows: list[dict] = []
    if not _BY_MODEL.is_dir():
        print(f"No {_BY_MODEL} — run run_multi_llm_benchmark.py first.")
        return

    for sub in sorted(_BY_MODEL.iterdir()):
        if not sub.is_dir() or sub.name == "__pycache__":
            continue
        rj = sub / "results.json"
        if not rj.is_file():
            continue
        try:
            data = json.loads(rj.read_text(encoding="utf-8"))
        except Exception as e:
            rows.append(
                {
                    "slug": sub.name,
                    "model_id": "?",
                    "label": sub.name,
                    "error": str(e),
                    "validation_pass_rate": 0.0,
                    "any_code_rate": 0.0,
                    "grade": "F",
                }
            )
            continue

        n = int(data.get("prompt_count") or 0)
        vp = int(data.get("validation_passed") or 0)
        ap = int(data.get("agent_returned_code") or 0)
        val_rate = (100.0 * vp / n) if n else 0.0
        code_rate = (100.0 * ap / n) if n else 0.0
        lens = [x["code_len"] for x in data.get("per_prompt", []) if x.get("ok")]
        mean_len = round(statistics.mean(lens), 0) if lens else 0.0

        row: dict = {
            "slug": sub.name,
            "model_id": data.get("model_id", ""),
            "label": data.get("label", sub.name),
            "prompt_count": n,
            "validation_passed": vp,
            "agent_returned_code": ap,
            "validation_pass_rate": round(val_rate, 1),
            "any_code_rate": round(code_rate, 1),
            "mean_valid_code_len": mean_len,
            "grade": _grade(val_rate),
        }
        fid = _load_fidelity(sub)
        if fid:
            row.update(fid)
        rows.append(row)

    rows_by_valid = sorted(rows, key=lambda r: (-r.get("validation_pass_rate", 0), r.get("slug", "")))
    rows_by_fidelity = sorted(
        rows,
        key=lambda r: (
            -(r.get("fidelity_mean") or -1.0),
            r.get("slug", ""),
        ),
    )

    _OUT_JSON.write_text(json.dumps(rows_by_valid, indent=2), encoding="utf-8")

    lines: list[str] = [
        "# LLM benchmark — validation and prompt-fidelity report",
        "",
        "Generated by `aggregate_benchmark_ratings.py` from `outputs/by_model/*/results.json` "
        "and merged with `outputs/by_model/*/accuracy_ratings.json` when present.",
        "",
        "## What the two scores measure",
        "",
        "### Syntactic validation (automated)",
        "",
        "Fraction of the **46** prompts where the returned `map()` passes `wgsl_validator` "
        "(allowed SDF helpers, balanced delimiters, no forbidden patterns). "
        "**Grade:** A≥90%, B≥75%, C≥50%, D≥25%, F<25%.",
        "",
        "### Prompt fidelity (manual code review)",
        "",
        "Mean score **1–5** per prompt: geometry and dimensions vs prompt intent (not just syntax). "
        "**0** = no usable output for that prompt. "
        "Per-prompt notes and rubric live in each model folder’s `accuracy_ratings.json`.",
        "",
        "**Bands:** B1 = prompts 1–10 (measured + steps), B2 = 11–20 (measured), "
        "B3 = 21–30 (no numbers), B4 = 31–40 (vague), B5 = 41–46 (organic).",
        "",
        "---",
        "",
        "## 1. Syntactic validation ranking",
        "",
        "| Rank | Model | Valid WGSL % | Any code % | Mean len (valid) | Grade |",
        "|------|-------|-------------|------------|------------------|-------|",
    ]
    for i, r in enumerate(rows_by_valid, 1):
        if "error" in r:
            lines.append(f"| {i} | `{r['slug']}` | — | — | — | {r['grade']} |")
            continue
        lines.append(
            f"| {i} | {r.get('label', r['slug'])} | **{r['validation_pass_rate']}%** | "
            f"{r['any_code_rate']}% | {r['mean_valid_code_len']:.0f} | {r['grade']} |"
        )

    lines.extend(
        [
            "",
            "## 2. Prompt-fidelity ranking",
            "",
            "| Rank | Model | Mean / 5 | B1 | B2 | B3 | B4 | B5 | # prompts @ score 0 |",
            "|------|-------|----------|----|----|----|----|----|------------------------|",
        ]
    )
    for i, r in enumerate(rows_by_fidelity, 1):
        if r.get("fidelity_mean") is None:
            lines.append(
                f"| {i} | {r.get('label', r['slug'])} | — | — | — | — | — | — | "
                f"*no accuracy_ratings.json* |"
            )
            continue
        bands = r.get("fidelity_bands") or {}
        bvals = [bands.get(k) for k in _BAND_KEYS]
        bstr = " | ".join(f"{v:.2f}" if v is not None else "—" for v in bvals)
        n0 = r.get("fidelity_no_output_count", 0)
        lines.append(
            f"| {i} | {r.get('label', r['slug'])} | **{r['fidelity_mean']:.2f}** | {bstr} | {n0} |"
        )

    lines.extend(
        [
            "",
            "## 3. Side-by-side summary",
            "",
            "Sorted by **prompt-fidelity mean** (highest first). Validation % shows whether the "
            "string-level checker agreed; it does not guarantee correct axes or dimensions.",
            "",
            "| Model | Valid WGSL % | Fidelity mean | Grade |",
            "|-------|-------------|---------------|-------|",
        ]
    )
    for r in rows_by_fidelity:
        if "error" in r:
            continue
        fm = r.get("fidelity_mean")
        fms = f"{fm:.2f}" if fm is not None else "—"
        lines.append(
            f"| {r.get('label', r['slug'])} | {r['validation_pass_rate']}% | {fms} | {r['grade']} |"
        )

    # Short takeaways
    with_fid = [r for r in rows if r.get("fidelity_mean") is not None and "error" not in r]
    best_f = max(with_fid, key=lambda r: r["fidelity_mean"]) if with_fid else None
    worst_f = min(with_fid, key=lambda r: r["fidelity_mean"]) if with_fid else None
    best_v = max(rows, key=lambda r: r.get("validation_pass_rate", 0)) if rows else None
    lines.extend(
        [
            "",
            "## 4. Short conclusions",
            "",
        ]
    )
    if best_v and "error" not in best_v:
        lines.append(
            f"- **Strongest validation pass rate:** {best_v.get('label')} at "
            f"{best_v['validation_pass_rate']}% (several models tie at 100%)."
        )
    if best_f:
        lines.append(
            f"- **Highest prompt-fidelity mean:** {best_f.get('label')} at **{best_f['fidelity_mean']:.2f}** / 5."
        )
    if worst_f and worst_f is not best_f:
        lines.append(
            f"- **Lowest prompt-fidelity mean:** {worst_f.get('label')} at **{worst_f['fidelity_mean']:.2f}** / 5 "
            f"({worst_f.get('fidelity_no_output_count', 0)} prompts scored 0 — no output)."
        )
    lines.append(
        "- **Validation vs fidelity:** a model can score 100% on `wgsl_validator` while still "
        "misplacing cylinders (wrong axis), halving dimensions, or using polar repetition for linear slots; "
        "check `accuracy_ratings.json` for those patterns."
    )
    lines.append("")

    lines.extend(
        [
            "## Model IDs (Hugging Face)",
            "",
        ]
    )
    for r in rows_by_valid:
        if r.get("model_id") and "error" not in r:
            lines.append(f"- **{r.get('label', r['slug'])}:** `{r['model_id']}`")

    lines.extend(
        [
            "",
            "## Source artifacts",
            "",
            "- `outputs/by_model/<slug>/results.json` — per-prompt validation and code lengths.",
            "- `outputs/by_model/<slug>/accuracy_ratings.json` — per-prompt fidelity scores and notes.",
            "",
        ]
    )

    _OUT_MD.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {_OUT_MD} and {_OUT_JSON} ({len(rows)} models)")


if __name__ == "__main__":
    main()
